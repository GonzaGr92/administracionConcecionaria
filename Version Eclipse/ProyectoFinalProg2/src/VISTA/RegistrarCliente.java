    private String[] llenaComboBoxAutos(ControlConcecionaria ctrl) throws SinAutoExcep {
        int cont = 0;
        int cont2 = 0;
        String[] arrAux;
        for (int i = 0; i < ctrl.getCtrlConcecionaria().getVehiculo().size(); i++) {
            if (ctrl.getCtrlConcecionaria().getVehiculo().get(i).getEstado().equals("En Venta")) {
                cont++;
            }
        }
        if (cont != 0) {
            arrAux = new String[cont];
            for (int i = 0; i < ctrl.getCtrlConcecionaria().getVehiculo().size(); i++) {
                if (ctrl.getCtrlConcecionaria().getVehiculo().get(i).getEstado().equals("En Venta")) {
                    arrAux[cont2] = ctrl.getCtrlConcecionaria().getVehiculo().get(i).getCodigo();
                    cont2++;
                }
            }
            return (arrAux);
        } else {
            throw new SinAutoExcep();
        }
    }

    private String[] llenaComboBoxCliente(ControlConcecionaria ctrl) throws SinClientesExcep {
        if (ctrl.getCtrlConcecionaria().getTiene().isEmpty()) {
            throw new SinClientesExcep();
        }else{
        String[] arreNombre = new String[ctrl.getCtrlConcecionaria().getTiene().size()];

        for (int i = 0; i < ctrl.getCtrlConcecionaria().getTiene().size(); i++) {
            arreNombre[i] = ctrl.getCtrlConcecionaria().getTiene().get(i).getApellido() + " " + ctrl.getCtrlConcecionaria().getTiene().get(i).getNombre();
        }
        return (arreNombre);
        }
    }
