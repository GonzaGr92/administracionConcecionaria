    private void aceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aceptarActionPerformed
        ControlIngresoClientes c1 = new ControlIngresoClientes(ctrl);
        try {
            if (nuevo.isSelected()) {
                c1.setValues(apellido, dni, direccion, nombre, localidad, telefono, ComboBoxVehiculo);
            }
            if (existe.isSelected()) {
                c1.relaciona(ComboBoxVehiculo, comboBoxCliente);
            }
            this.dispose();
        } catch (ConversionExcep ex) {
            error.setText(ex.getMessage());
        } catch (AnioExcep ex) {
            error.setText(ex.getMessage());
        } catch (CamposNulosExcep ex) {
            error.setText(ex.getMessage());
        }
    }//GEN-LAST:event_aceptarActionPerformed

    private void buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarActionPerformed
        try {
            CodigoExcep.validarCodigo(Integer.parseInt(codigoBusqueda.getText()), ctrl);
            ConsultaCodigo cc = new ConsultaCodigo(Integer.parseInt(codigoBusqueda.getText()), ctrl);
            cc.setVisible(true);
        } catch (NumberFormatException e) {
            error.setText("ERROR: Ingrese un codigo de busqueda valido");
        } catch (CodigoExcep ex) {
            error.setText(ex.getMessage());
        }
    }//GEN-LAST:event_buscarActionPerformed
