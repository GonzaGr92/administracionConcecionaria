    private Vehiculo irVehiculo(int i) {
        return ((Vehiculo) ctrl.getCtrlConcecionaria().getVehiculo().get(i));
    }

    private Auto irAuto(int i) {
        return ((Auto) ctrl.getCtrlConcecionaria().getVehiculo().get(i));
    }

    private String busquedaCliente(int codigo) {
        String nombre = null;
        for (int i = 0; i < ctrl.getCtrlConcecionaria().getTiene().size(); i++) {
            for (int j = 0; j < ctrl.getCtrlConcecionaria().getTiene().get(i).getCompro().size(); j++) {
                if (codigo == Integer.parseInt(ctrl.getCtrlConcecionaria().getTiene().get(i).getCompro().get(j).getCodigo())) {
                    nombre = ctrl.getCtrlConcecionaria().getTiene().get(i).getApellido()+" "+ctrl.getCtrlConcecionaria().getTiene().get(i).getNombre();
                }
            }
        }
        return nombre;
    }

    private String gnc(int codigo) {
        if (irAuto(codigo).isGnc() == true) {
            return ("GNC");
        } else {
            return (null);
        }
    }

    private Camioneta irCamioneta(int i) {
        return ((Camioneta) ctrl.getCtrlConcecionaria().getVehiculo().get(i));
    }

    private String traccion(int codigo) {
        if (irCamioneta(codigo).isTraccion()) {
            return ("4x4");
        }else{
            return ("4x2");
        }
    }

    private Motocicleta irMoto(int i) {
        return ((Motocicleta) ctrl.getCtrlConcecionaria().getVehiculo().get(i));
    }
