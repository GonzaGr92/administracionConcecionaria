    public ConsultaCodigo(int codigo, ControlConcecionaria ctrl) {
        initComponents();
        this.ctrl = ctrl;
        tipo.setText(irVehiculo(codigo).getEs().toUpperCase());
        estado.setText("Estado: " + irVehiculo(codigo).getEstado());
        marca.setText("Marca: " + irVehiculo(codigo).getMarca());
        modelo.setText("Modelo: " + irVehiculo(codigo).getModelo());
        patente.setText("Patente: " + irVehiculo(codigo).getPatente());
        anio.setText("AÃ±o: " + Integer.toString(irVehiculo(codigo).getAnio()));
        kilometraje.setText("Km: " + Integer.toString(irVehiculo(codigo).getKilometraje()));
        
        if ((irVehiculo(codigo).getEstado()).equals("Vendido")) {
            precio.setText("Precio: --");
            duenio.setText("Propietario: "+busquedaCliente(codigo).toUpperCase());
        } else {
            precio.setText("Precio:" + Integer.toString(irVehiculo(codigo).getPrecio()));
        }

        if (consultaEs(codigo).equals("automovil")) {
            op1.setText("Combustible: " + irAuto(codigo).getCombustible());
            op2.setText(gnc(codigo));
            op3.setText("Puertas: " + irAuto(codigo).getCantidadPuertas());
            op4.setText("Motor: " + Float.toString(irAuto(codigo).getCilmotor()));

        } else {
            if (consultaEs(codigo).equals("camioneta")) {
                op1.setText("Cabina: "+irCamioneta(codigo).getCabina());
                op2.setText("Traccion: "+traccion(codigo));
            } else {
                if (consultaEs(codigo).equals("motocicleta")) {
                    op1.setText("Cilindrada: "+irMoto(codigo).getCilindrada());
                }
            }
        }
    }

    private String consultaEs(int i) {
        return (ctrl.getCtrlConcecionaria().getVehiculo().get(i).getEs().toLowerCase());
    }

